    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        // TODO add your handling code here:
        
        Operaciones calc =  new Operaciones();
        
        PPF_Monto m = new PPF_Monto();
        
        double num1 = Double.parseDouble(TcCapital.getText());
        calc.setCapital(num1);
        
        double num2 = Double.parseDouble(TcInteres.getText());
        calc.setInteres(num2);
        
        double num3 = Double.parseDouble(TcTiempo.getText());
        calc.setTiempo(num3);
        
        String interescompuesto = (Double.toString(calc.InteresCompuesto(num1, num2, num3)));
        
        String resultIc = (Double.toString(calc.InteresCompResol(num1,num2,num3)));
        
        // Segunda Ventana Reflejando datos colocados por el usuario
        
        PPF_Monto ventanaM = new PPF_Monto();
        
        ventanaM.setVisible(true);
        
        ventanaM.TMonto.setText(resultIc);
        
        ventanaM.TCapital.setText(Double.toString(num1));
        
        ventanaM.TInteres.setText(interescompuesto);
        
        // Tercera Ventana Reflejando tabla
        
        
    }//GEN-LAST:event_jButton1MouseClicked

